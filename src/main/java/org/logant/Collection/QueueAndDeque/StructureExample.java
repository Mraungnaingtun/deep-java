package org.logant.Collection.QueueAndDeque;

/*
 * Storing Elements in Stacks and Queues
 * Stacks -> LIFO
 * Queues -> FIFO
 * 
 */

public class StructureExample {

    /*
     * Queue Methods: add(), offer(), poll(), peek()
     * --------------------------------------------------------------------------
     * Deque FIFO Methods: addLast(), offerLast(), removeFirst(), pollFirst(),
     * getFirst(), peekFirst()
     * --------------------------------------------------------------------------
     * Deque LIFO Methods: addFirst(), offerFirst(), removeFirst(), pollFirst(),
     * getFirst(), peekFirst()
     * --------------------------------------------------------------------------
     * Common Deque Methods: push(), pop(), poll(), peek()
     * 
     */

    /*
     * 
     * Pushing - Stack ထဲသို့ ဒေတာတစ်ခု ထည့်ခြင်း။
     * Stack သို့ အချက်အလက်အသစ်တစ်ခုကို ထည့်ခြင်းကို "Push" လို့ခေါ်ပါတယ်။
     * --------------------------------------------------------------
     * Popping - Stack မှာရှိတဲ့ အဆုံးဆုံး ဒေတာတစ်ခုကို ဖယ်ရှားခြင်း။
     * Stack အပေါ်ဆုံးမှာရှိတဲ့ အချက်အလက်ကို ဖယ်ရှားခြင်းကို "Pop" လို့ခေါ်ပါတယ်။
     * --------------------------------------------------------------
     * Peeking - Stack အပေါ်ဆုံးရှိတဲ့ အချက်အလက်ကို ကြည့်ခြင်း။
     * Stack အပေါ်ဆုံးမှာရှိတဲ့ အချက်အလက်ကို ဖယ်ရှားမပါပဲ၊ ချက်ချင်းကြည့်ခြင်းကို
     * "Peek" လို့ခေါ်ပါတယ်။
     * --------------------------------------------------------------
     */
}
